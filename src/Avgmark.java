public class Avgmark {
    public static void main(String[] args) {
        int n=5,avg=0;
        int arr[]=new int[n];
        arr[0]=70;
        arr[1]=90;
        arr[2]=100;
        arr[3]=60;
        arr[4]=75;
        for (int i=0;i<=0;i++){
            avg=avg+arr[i];
            System.out.println("Average of ("+arr[0]+","+arr[1]+","+arr[2]+","+arr[3]+","+arr[4]+")is="+avg/n);
        }
    }
}
